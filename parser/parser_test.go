func testDecimalLiteral(t *testing.T, il ast.Expression, value decimal.Decimal) bool {
	dec, ok := il.(*ast.DecimalLiteral)
	if !ok {
		t.Errorf("il not *ast.DecimalLiteral. got=%T", il)
		return false
	}

	if !dec.Value.Equal(value) {
		t.Errorf("integ.Value not %d. got=%d", value, dec.Value)
		return false

	}

	if dec.TokenLiteral() != value.String() {
		t.Errorf("dec.TokenLiteral not '%s'  got '%s'", value,
			dec.TokenLiteral())
		return false
	}

	return true
}


